#include <iostream>

#include "graph.h"
#include "read.h"

int main(int argc, char *argv[]) {

  Read<graph> readGraph("GRAFO1.txt");

  graph *g = readGraph.getGraph();

  std::cout
      << "\n\n**************************** PRINTING GRAPH ****************"
         "*******************************\n\n";
  g->pprint();
  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  std::cout
      << "\n\n**************************** MEMORY ANALYSIS ****************"
         "*******************************\n\n";
  g->analyzeMemory();

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  std::cout << "\n\n**************************** BELLMAN FORD ****************"
               "*******************************\n\n";
  g->bellmanFord(0);

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";
  std::cout << "\n\n**************************** DIJKSTRA ****************"
               "*******************************\n\n";
  g->dijkstra(0);

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";
  std::cout << "\n\n**************************** GREEDY BFS ****************"
               "*******************************\n\n";
  g->greedyBFS(0, 7);

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  std::cout << "\n\n**************************** A* ****************"
               "*******************************\n\n";
  g->aEstrella(0, 7).pprint();

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  std::cout
      << "\n\n**************************** FLOYD WARSHALL ****************"
         "*******************************\n\n";
  g->floydWarshall();

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  std::cout << "\n\n**************************** DESTRUCTION  ****************"
               "*******************************\n\n";
  delete g;

  std::cout << "\n\n***********************************************************"
               "*******************************\n\n";

  return 0;
}
